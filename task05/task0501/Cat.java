public class Cat {
    String name;
    int age;
    int weight;
    int strength;
    public Cat (String name, int age, int weight, int strength) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.strength = strength;
        System.out.println(name + " " + age + " " + weight + " " + strength);
    }
}
